module tb_rgb2gray;

logic clk;

logic [7:0] r;
logic [7:0] g;
logic [7:0] b;

logic [7:0] gray;

/////////////////////////////////////////////////
// DUT
/////////////////////////////////////////////////

rgb2gray dut(

    .clk(clk),

    .r(r),
    .g(g),
    .b(b),

    .gray(gray)

);

/////////////////////////////////////////////////
// Clock
/////////////////////////////////////////////////

always #5 clk = ~clk;

/////////////////////////////////////////////////
// Test
/////////////////////////////////////////////////

initial
begin

    clk = 0;

    r = 8'd100;
    g = 8'd150;
    b = 8'd200;

    #20;

    r = 8'd255;
    g = 8'd255;
    b = 8'd255;

    #20;

    $stop;

end

endmodule