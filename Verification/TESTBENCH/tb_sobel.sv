`timescale 1ns/1ps

module tb_sobel;

logic clk;

logic [7:0] pixel_in;
logic [7:0] edge_out;

integer file;
integer status;

/////////////////////////////////////////////////
// RTL Output File
/////////////////////////////////////////////////

integer outfile;

/////////////////////////////////////////////////
// DUT
/////////////////////////////////////////////////

sobel_filter dut(

    .clk(clk),

    .pixel_in(pixel_in),

    .edge_out(edge_out)

);

/////////////////////////////////////////////////
// Clock
/////////////////////////////////////////////////

always #5 clk = ~clk;

/////////////////////////////////////////////////
// Simulation
/////////////////////////////////////////////////

initial
begin

    clk = 0;

    //////////////////////////////////////////////////
    // Open MATLAB Pixel File
    //////////////////////////////////////////////////

    file = $fopen(
    "../../verification/vectors/input_pixels.txt",
    "r");

    //////////////////////////////////////////////////
    // Open RTL Output File
    //////////////////////////////////////////////////

    outfile = $fopen(
    "rtl_output.txt",
    "w");

    //////////////////////////////////////////////////
    // Error Check
    //////////////////////////////////////////////////

    if(file == 0)
    begin

        $display("ERROR: Cannot open pixel file");

        $stop;

    end

    //////////////////////////////////////////////////
    // Read Pixels
    //////////////////////////////////////////////////

    while(!$feof(file))
    begin

        //////////////////////////////////////////////////
        // Read Pixel
        //////////////////////////////////////////////////

        status = $fscanf(file,"%d\n",pixel_in);

        //////////////////////////////////////////////////
        // Wait for Processing
        //////////////////////////////////////////////////

        #10;

        //////////////////////////////////////////////////
        // Save RTL Output
        //////////////////////////////////////////////////

        $fwrite(outfile,"%d\n",edge_out);

    end

    //////////////////////////////////////////////////
    // Close Files
    //////////////////////////////////////////////////

    $fclose(file);

    $fclose(outfile);

    //////////////////////////////////////////////////
    // Finish
    //////////////////////////////////////////////////

    $display("RTL Simulation Completed");

    $finish;

end

endmodule