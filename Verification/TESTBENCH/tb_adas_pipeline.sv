`timescale 1ns/1ps

module tb_adas_pipeline;

//////////////////////////////////////////////////
// Signal Declarations
//////////////////////////////////////////////////

logic clk;
logic rst_n;

logic [7:0] camera_r;
logic [7:0] camera_g;
logic [7:0] camera_b;

logic [7:0] edge_out;

//////////////////////////////////////////////////
// File Variables
//////////////////////////////////////////////////

integer file;
integer pixel;

//////////////////////////////////////////////////
// Clock Generation
//////////////////////////////////////////////////

always #5 clk = ~clk;

//////////////////////////////////////////////////
// DUT Instance
//////////////////////////////////////////////////

adas_pipeline_top DUT (

    .clk(clk),
    .rst_n(rst_n),

    .r(camera_r),
    .g(camera_g),
    .b(camera_b),

    .edge_out(edge_out)

);

//////////////////////////////////////////////////
// Main Test Sequence
//////////////////////////////////////////////////

initial
begin

    //////////////////////////////////////////////
    // Initialize Signals
    //////////////////////////////////////////////

    clk = 0;

    rst_n = 0;

    camera_r = 0;
    camera_g = 0;
    camera_b = 0;

    //////////////////////////////////////////////
    // Reset Delay
    //////////////////////////////////////////////

    #20;

    rst_n = 1;

    //////////////////////////////////////////////
    // Open MATLAB Pixel File
    //////////////////////////////////////////////

    file = $fopen(
    "D:/VLSI/FPGA_ADAS_Vision_Accelerator/verification/vectors/input_pixels.txt",
    "r");

    //////////////////////////////////////////////
    // Check File Open
    //////////////////////////////////////////////

    if(file == 0)
    begin

        $display("ERROR: Cannot open input_pixels.txt");

        $stop;

    end

    //////////////////////////////////////////////
    // Read Pixels From File
    //////////////////////////////////////////////

    while(!$feof(file))
    begin

        void'($fscanf(file,"%d\n",pixel));

        $display("Pixel = %d", pixel);

        camera_r = pixel[7:0];
        camera_g = pixel[7:0];
        camera_b = pixel[7:0];

        #10;

    end

    //////////////////////////////////////////////
    // Close File
    //////////////////////////////////////////////

    $fclose(file);

    //////////////////////////////////////////////
    // Simulation Complete
    //////////////////////////////////////////////

    #100;

    $display("FPGA ADAS Simulation Completed");

    $finish;

end

//////////////////////////////////////////////////
// Waveform Dump
//////////////////////////////////////////////////

initial
begin

    $dumpfile("tb_adas_pipeline.vcd");

    $dumpvars(0,tb_adas_pipeline);

end

endmodule